document.addEventListener('DOMContentLoaded', () => {
    const defaultStat = 'wrcplus'; // 初期表示の指標
    const team1Selector = document.getElementById('team1Selector');
    const player1Selector = document.getElementById('player1Selector');
    const team2Selector = document.getElementById('team2Selector');
    const player2Selector = document.getElementById('player2Selector');
    const statSelector = document.getElementById('statSelector');

    // 初期の球団選択をここに出す
    updatePlayerOptions('team1Selector', 'player1Selector');
    updatePlayerOptions('team2Selector', 'player2Selector');

    // 初期のグラフ描画
    renderComparisonChart(defaultStat, player1Selector.value, player2Selector.value);

    // 球団選択変更→選手をその球団に更新するやつ
    team1Selector.addEventListener('change', () => {
        updatePlayerOptions('team1Selector', 'player1Selector');
        renderComparisonChart(statSelector.value, player1Selector.value, player2Selector.value);
    });
    //上と同じ第二選択の球団
    team2Selector.addEventListener('change', () => {
        updatePlayerOptions('team2Selector', 'player2Selector');
        renderComparisonChart(statSelector.value, player1Selector.value, player2Selector.value);
    });

    // 選手と指標の変更に応じてグラフを更新する
    player1Selector.addEventListener('change', () => {
        renderComparisonChart(statSelector.value, player1Selector.value, player2Selector.value);
    });

    player2Selector.addEventListener('change', () => {
        renderComparisonChart(statSelector.value, player1Selector.value, player2Selector.value);
    });

    statSelector.addEventListener('change', () => {
        renderComparisonChart(statSelector.value, player1Selector.value, player2Selector.value);
    });
});
