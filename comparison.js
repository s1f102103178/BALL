// 球団選択に基づいて選手を更新
function updatePlayerOptions(teamSelectorId, playerSelectorId) {
    const teamSelector = document.getElementById(teamSelectorId);
    const playerSelector = document.getElementById(playerSelectorId);
    const selectedTeam = teamSelector.value;


    // デバッグ用ログを追加
    console.log("Selected Team:", selectedTeam); // 選択された球団名
    console.log("Players for the team:", teams[selectedTeam]); // 該当球団の選手リスト


    // 選択された球団に応じた選手を更新

    playerSelector.innerHTML = ""; // 選手セレクトボックスをクリア
    if (!teams[selectedTeam]) {
        console.error("選択された球団に対応するデータが見つかりません:", selectedTeam);
        return;
    }

    Object.keys(teams[selectedTeam]).forEach(playerName => {
        const playerKey = teams[selectedTeam][playerName];
        const option = document.createElement("option");
        option.value = playerKey;
        option.textContent = playerName;
        playerSelector.appendChild(option);
        
    });
    // デバッグ用: 選手リストが正常に更新されたか確認
    console.log("Updated Player Selector Options:", playerSelector.innerHTML);
}

// グラフ描画ロジック
function renderComparisonChart(statType, player1, player2) {
    const player1Data = playerStats[player1];
    const player2Data = playerStats[player2];

    // データの存在を確認
    if (!player1Data || !player2Data) {
        console.error("選手データが見つかりません:", { player1, player2 });
        return;
    }

    const years = Object.keys(player1Data); // 年度
    const player1Values = years.map(year => player1Data[year]?.[statType] || null);
    const player2Values = years.map(year => player2Data[year]?.[statType] || null);

    const traces = [
        {
            x: years,
            y: player1Values,
            mode: 'lines+markers',
            name: player1.toUpperCase(),
            line: { color: 'blue' }
        },
        {
            x: years,
            y: player2Values,
            mode: 'lines+markers',
            name: player2.toUpperCase(),
            line: { color: 'red' }
        }
    ];

    const layout = {
        title: `${statType.toUpperCase()} の比較 (${player1.toUpperCase()} vs ${player2.toUpperCase()})`,
        xaxis: { title: '年度' },
        yaxis: { title: statType.toUpperCase() }
    };

    Plotly.newPlot('comparisonChart', traces, layout);
}


